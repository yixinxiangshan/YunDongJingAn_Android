package com.ecloudiot.framework.widget.model;

public class ECButtonModel {
	private String name;
	private String background;
	private String text;
	private String clickable;
	private String focusable;
	private String clickTag;
	private String longClickTag;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBackground() {
		return background;
	}
	public void setBackground(String background) {
		this.background = background;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getClickable() {
		return clickable;
	}
	public void setClickable(String clickable) {
		this.clickable = clickable;
	}
	public String getFocusable() {
		return focusable;
	}
	public void setFocusable(String focusable) {
		this.focusable = focusable;
	}
	public String getClickTag() {
		return clickTag;
	}
	public void setClickTag(String clickTag) {
		this.clickTag = clickTag;
	}
	public String getLongClickTag() {
		return longClickTag;
	}
	public void setLongClickTag(String longClickTag) {
		this.longClickTag = longClickTag;
	}
	

}
